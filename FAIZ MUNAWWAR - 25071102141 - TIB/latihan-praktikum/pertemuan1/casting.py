# Mengkonversi dari string ke integer
string = 3.2
string_to_number = int(string)

print(type(string_to_number))
print(string)

# Contoh Simpel mengkonversi ke float
x = float(1)     # x will be 1.0
y = float(2.8)   # y will be 2.8
z = float("3")   # z will be 3.0
w = float("4.2") # w will be 4.2

print(x)
print(y)
print(z)
print(w)

# Contoh Simpel Mengkonversi menjadi int,str,float
x = str(1)   # x will be 1
y = float(2.8) # y will be 2
z = int("3") # z will be 3

print(x)
print(y)
print(z)