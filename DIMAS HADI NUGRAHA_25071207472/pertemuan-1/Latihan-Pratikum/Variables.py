'''variabel pada python'''
#variabel adalah tempat untuk menyimpan data atau nilai
#contoh1 membuat variabel dan mengisi nilai 
nama = 'dimas hadi nugraha'  #variabel nama berisi string 'dimas hadi nugraha'
umur = 21                    #variabel umur berisi integer 21
tinggi = 172.5               #variabel tinggi berisi float 172.5

#menampilkan nilai dari variabel
print(nama)   #hasilnya adalah dimas hadi nugraha
print(umur)   #hasilnya adalah 21   
print(tinggi) #hasilnya adalah 172.5

#contoh2 mwngunakan type() untuk menampilkan tipe data dari variabel
print(type(nama))   #hasilnya adalah <class 'str'>  
print(type(umur))   #hasilnya adalah <class 'int'>  
print(type(tinggi)) #hasilnya adalah <class 'float'>       

