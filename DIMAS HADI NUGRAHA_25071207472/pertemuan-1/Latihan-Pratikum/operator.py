'''Operator pada python'''
#Operator adalah simbol yang digunakan untuk melakukan operasi pada nilai atau variabel
#contoh1 operator penjumlahan
sum1 = 15 + 20     #menjumlahkan 15 dan 20
sum2 = sum1 + 100  #menjumlahkan hasil dari sum1 dengan 100
sum3 = sum2 + 500  #menjumlahkan hasil dari sum2 dengan 500
print(sum3)        #hasilnya adalah 635 karena 15 + 20 + 100 + 500 = 635

#arimatika
#contoh2 operator aritmatika
x = 15
y = 4

print(x + y)  #hasilnya adalah 19 karena 15 + 4 = 19
print(x - y)  #hasilnya adalah 11 karena 15 - 4 = 11
print(x * y)  #hasilnya adalah 60 karena 15 * 4 = 60
print(x / y)  #hasilnya adalah 3.75 karena 15 / 4 = 3.75
print(x % y)  #hasilnya adalah 3 karena sisa dari 15 / 4 = 3
print(x ** y) #hasilnya adalah 50625 karena 15 pangkat 4 = 50625
print(x // y) #hasilnya adalah 3 karena pembagian bulat dari 15 / 4 = 3

