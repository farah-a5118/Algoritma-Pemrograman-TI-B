'''python booleans'''
  # Boolean adalah tipe data yang hanya memiliki dua nilai yaitu True dan False
'''boolean value'''
  #bool juga bisa digunakan untuk membandingkan sebuah nilai dan boolean akan mengembalikan nilai True atau False
#contoh1
a = 10
b = 5      
print(a > b)   # akan mengembalikan nilai True karena 10 lebih besar dari 5

#contoh2
print(2>3)# akan mengembalikan nilai False karena 2 tidak lebih besar dari 3
print(2==3)# akan mengembalikan nilai False karena 2 tidak sama dengan 3
print(2<3)# akan mengembalikan nilai True karena 2 lebih kecil dari 3
'''mengkonversi nilai ke boolean'''
  #kita juga bisa mengkonversi nilai lain ke boolean menggunakan fungsi bool()
print(bool(5)) # akan mengembalikan nilai True karena angka selain 0,
print(bool(0)) # akan mengembalikan nilai False karena angka 0,
