'''jenis casting pada python adalah mengubah dari satu tipe data ke tipe data lainnya'''
#contoh casting ke integer
x = 5.7     
y = int(x)
print(y)  #hasilnya adalah 5 karena pembulatan ke bawah pada casting ke integer     
#contoh casting ke float
a = 10          
b = float(a)
print(b)  #hasilnya adalah 10.0 karena sudah diubah ke tipe data float
#contoh casting ke string       
c = 15
d = str(c)
print(d)  #hasilnya adalah '15' karena sudah diubah ke tipe data string 

