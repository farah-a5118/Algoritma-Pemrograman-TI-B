#1. Specify a Variable Type
'''
Casting dilakukan untuk membuat variabel dengan tipe data yang spesifik'
'''

x = '1' # Str
y = 2.0 # Float
z = 55  # Int

'''
Mengubah menjadi bertipe int.
'''
a = int('1') #Menghasilkan nilai 1
b = int(2.0) #Menghasilkan nilai 2
c = int(55)  #Menghasilkan nilai 55

print("--Integer--")
print(a)
print(b)
print(c)

'''
Mengubah menjadi bertipe float.
'''
d = float('1') #Menghasilkan 1.0
e = float(2.0) #Menghasilkan 2.0
f = float(55)  #Menghasilkan 55.0

print("\n--Float--")
print(d)
print(e)
print(f)

'''
Mengubah menjadi bertipe string
'''
g = str('1') #Menghasilkan 1
h = str(2.0) #Menghasilkan 2
i = str(55)  #Menghasilkan 55

print("\n--String--")
print(g) 
print(h)
print(i)