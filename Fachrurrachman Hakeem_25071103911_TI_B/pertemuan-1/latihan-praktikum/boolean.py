#boolean punya dua pilihan yaitu true or false yang biasa dipakai dalam if statements
is_student = True #true or false harus ditulis dengan huruf kapital untuk awal hurufnya. True & False
print(f"are you a student?: {is_student}")

if is_student:
    print("you are a student")
else:
    print("you are not a student")


